//Unary increment and decrement operators

let nn1 = 10;
let nn2 = 10;

console.log(nn1); 
console.log(nn1++); 
console.log(nn1); 

console.log(nn2); 
console.log(++nn2); 
console.log(nn2); 

let nn3 = 20;
let nn4 = 20;

console.log(nn3); 
console.log(nn3--); 
console.log(nn3); 

console.log(nn4); 
console.log(--nn4); 
console.log(nn4); 

console.log(0.2 + 0.1); 
console.log(0.2 * 0.1); 
console.log(0.3 / 0.1); 